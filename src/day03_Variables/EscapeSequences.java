package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Everyone");
        System.out.println("My name is Alex, Iam from Virginia");

        System.out.println("Hello Everyone\nMy name is Alex, I am from Virginia");

        System.out.println("============================");

        System.out.println("\tToday i learned java");
        System.out.println("it was so much fun");

        System.out.println("============================");

        System.out.println("/");   // print single front slash
        System.out.println("\\"); //print single backslash


        System.out.println("my name is 'alex'");


        System.out.println("============================");

        System.out.println("my favorite show is \"GOT\"");



    }


}
