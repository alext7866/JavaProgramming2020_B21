package day19_ForLoop;

public class TaskForLunch {

    public static void main(String[] args) {
        // 6. write a program that can print a ~ z in same line sperated by space

        for (char i = 'a'; i <= 'z'; i++){
            System.out.print(i + " ");
        }

        System.out.println();

        System.out.println("=====================");


        //7. write a program that can print A ~ Z in same line sperated by space

        for(char i = 'A'; i <= 'Z'; i++){
            System.out.print(i + " ");

        }

        System.out.println();

        System.out.println("=====================");

       // 8. print out the first 30000 characters in same line seperated by space

        for(char i = 1; i <= 30000; i++){
            System.out.print(i + " ");
        }
    }
}




